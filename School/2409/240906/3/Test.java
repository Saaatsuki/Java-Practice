public class Test {
    public static void main(String[] args) {
        // int型としてbarを宣言して出力
        int bar = 3;
        System.out.println(bar);  // 出力: 3

        // float型としてbarを再宣言して出力
        float barFloat = 3.0f;
        System.out.println(barFloat);  // 出力: 3.0

        // String型としてbarを再宣言して出力
        String barString = "Hello";
        System.out.println(barString);  // 出力: Hello
    }
}
