public class Alice {
    public static void main(String[] args) {
        // 文字列型の変数 name を宣言し、"Alice" で初期化
        String name = "Alice";
        // 文字列型の変数 greeting を宣言し、"Hello, World!" で初期化
        String greeting = "Hello, World!";

        // 変数 name の値を表示
        System.out.println("Name: " + name);
        // 変数 greeting の値を表示
        System.out.println("Greeting: " + greeting);
    }
}
